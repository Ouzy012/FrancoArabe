public class Evaluation {
    
    private int coef;
    private float devoir;
    private float moyenneCC;
    private float composition;
    private String nomMatiere;
    private String nomArabe;
    private int rang;
    private String appreciations;
    private float moyX;

    public int getCoef() {
        return coef;
    }

    public void setCoef(int coef) {
        this.coef = coef;
    }

    public float getDevoir() {
        return devoir;
    }

    public void setDevoir(float devoir) {
        this.devoir = devoir;
    }

    public float getMoyenneCC() {
        return moyenneCC;
    }

    public void setMoyenneCC(float moyenneCC) {
        this.moyenneCC = moyenneCC;
    }

    public float getComposition() {
        return composition;
    }

    public void setComposition(float composition) {
        this.composition = composition;
    }

    public String getNomMatiere() {
        return nomMatiere;
    }

    public void setNomMatiere(String nomMatiere) {
        this.nomMatiere = nomMatiere;
    }

    public int getRang() {
        return rang;
    }

    public void setRang(int rang) {
        this.rang = rang;
    }

    public String getAppreciations() {
        return appreciations;
    }

    public void setAppreciations(String appreciations) {
        this.appreciations = appreciations;
    }

    public float getMoyX() {
        return moyX;
    }

    public void setMoyX(float moyX) {
        this.moyX = moyX;
    }

    public String getNomArabe() {
        return nomArabe;
    }

    public void setNomArabe(String nomArabe) {
        this.nomArabe = nomArabe;
    }
    
    
}
