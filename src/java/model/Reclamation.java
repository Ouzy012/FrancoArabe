public class Reclamation {
    private String message;
    private String enTete;
    private String loginEleve;
    private String loginProf;
    private String date;
    private String idReclamation;

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getEnTete() {
        return enTete;
    }

    public void setEnTete(String enTete) {
        this.enTete = enTete;
    }

    public String getLoginEleve() {
        return loginEleve;
    }

    public void setLoginEleve(String loginEleve) {
        this.loginEleve = loginEleve;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getLoginProf() {
        return loginProf;
    }

    public void setLoginProf(String loginProf) {
        this.loginProf = loginProf;
    }

    public String getIdReclamation() {
        return idReclamation;
    }

    public void setIdReclamation(String idReclamation) {
        this.idReclamation = idReclamation;
    }


    
    
}
